public class Student {
    private String name;

    public void setName(String newName) {
        name = newName;
    }
    private int id;
    
    public void setId(int newId) {
        id = newId;
    }
    private double grade;
    public void setGrade(double newGrade) {
        grade = newGrade;
    }

    public String getName() {
        return name;
    }
    
    public int getId() {
        return id;
    }
    
    public double getGrade() {
        return grade;
    }
    
}
